# STUDENT INSTRUCTIONS — AP CSP CREATE TASK WORKFLOW (Feb 9–25, 2026)
# Purpose:
# This timeline guides you through completing all required components of the AP CSP Create Performance Task. Follow each phase carefully and check off tasks as you complete them.
# PHASE 1 — BUILD (Feb 9–14)
# 1. Finalize Program Purpose & Functionality
# Confirm your program solves a meaningful problem or performs a clear function
# Make sure it includes input and output
# Ensure your idea meets all AP requirements
# 2. Build Your List Functionality
# Create at least one list
# Store data in it (ex: jokes, scores, feedback, options)
# Use the list in your program (access, modify, append, remove, etc.)
# 3. Develop Your Student‑Created Procedure
# Your procedure must include:
# A parameter
# Sequencing (multiple steps)
# Selection (if/else)
# Iteration (loop)
# A meaningful purpose in your program
# PHASE 2 — POLISH (Feb 15–20)
# 4. Debug & Improve Your Program
# Fix errors
# Improve clarity
# Test all inputs and outputs
# 5. Add Comments + Export Code as PDF
# Add meaningful comments
# Credit any reused or inspired code
# Export your full program as a PDF
# PHASE 3 — VIDEO + PPR (Feb 18–22)
# 6. Record Your Video (30–60 seconds)
# Your video must show:
# Input → Output
# Your list being used
# Your procedure being called
# Captions explaining what’s happening
# 7. Capture Screenshots for Your PPR
# You need:
# Screenshot of your procedure (name, parameters, sequencing, selection, iteration)
# Screenshot of where the procedure is called
# Screenshot of list creation
# Screenshot of list use
# 8. Write Your Reflection (3–5 sentences)
# Explain:
# How your program meets the Create Task requirements
# What improvements you made
# Why your list and procedure matter
# PHASE 4 — SUBMIT (Feb 23–25)
# 9. Assemble All Artifacts
# Code PDF
# Video
# PPR screenshots
# Reflection
# 10. Practice Uploading to AP Digital Portfolio
# Upload each component
# Confirm file types and sizes
# Verify everything is final
# 11. Final Review & Check‑In
# Review your work with the rubric
# Confirm all requirements are met
# Ask questions before final submission
